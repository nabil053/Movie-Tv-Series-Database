    private void PlatformSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PlatformSelectActionPerformed
        String PlatformSelected = PlatformSelect.getSelectedItem().toString();
        String GenreSelected = GenreSelect.getSelectedItem().toString();
        String query = "SELECT id,name,covers FROM content";
        if((PlatformSelected.equals("all")==false)||(GenreSelected.equals("all")==false)){
            query = query+" WHERE";
        }
        if(PlatformSelected.equals("all")==false){
            query = query+" content_type='"+PlatformSelected+"'";
        }
        if((PlatformSelected.equals("all")==false)&&(GenreSelected.equals("all")==false)){
            query = query+" AND";
        }
        if(GenreSelected.equals("all")==false){
            query = query+" genre='"+GenreSelected+"'";
        }
        query = query+" ORDER BY id DESC";
        System.out.println(query);
        DisplayData(query);
        this.pack();
    }//GEN-LAST:event_PlatformSelectActionPerformed

