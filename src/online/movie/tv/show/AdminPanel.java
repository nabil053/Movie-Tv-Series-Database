    private void viewPostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewPostActionPerformed
          
         if (postType.getSelectedIndex()==0) {
            ViewMoviePost moviePost = new ViewMoviePost();
            moviePost.setVisible(true);
            moviePost.pack();
            moviePost.setLocationRelativeTo(null);
            moviePost.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            this.dispose();
        }else if (postType.getSelectedIndex()==1){
            ViewTvSeriesPost tvSeriesPost = new ViewTvSeriesPost();
            tvSeriesPost.setVisible(true);
            tvSeriesPost.pack();
            tvSeriesPost.setLocationRelativeTo(null);
            tvSeriesPost.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            this.dispose();
        }
       
    }//GEN-LAST:event_viewPostActionPerformed

