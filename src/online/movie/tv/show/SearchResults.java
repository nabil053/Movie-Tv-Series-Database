    public SearchResults(String query) {
        initComponents();
        LookAndFeel();
        setResizable(false);
        setLocationRelativeTo(null);
        setVisible(true);
         this.pack();
        db = new ContentDatabaseHandler();
        db.connectDatabase();
        
        results = db.getContent(query);
        
        try{
            int i=0;
            while(results.next()){
                String name = results.getString("name");
                int id = results.getInt("id");
                
                
                switch(i){
                    case 0:Result1.setText(name);
                        Id1 = id;
                        break;
                    case 1:Result2.setText(name);
                        Id2 = id;
                        break;
                    case 2:Result3.setText(name);
                        Id3 = id;
                        break;
                    case 3:Result4.setText(name);
                        Id4 = id;
                        break;
                    case 4:Result5.setText(name);
                        Id5 = id;
                        break;
                    case 5:Result6.setText(name);
                        Id6 = id;
                        break;
                    case 6:Result7.setText(name);
                        Id7 = id;
                        break;
                    case 7:Result8.setText(name);
                        Id8 = id;
                        break;
                }
                i++;
            }
            if(i<1)Result1.setVisible(false);
            if(i<2)Result2.setVisible(false);
            if(i<3)Result3.setVisible(false);
            if(i<4)Result4.setVisible(false);
            if(i<5)Result5.setVisible(false);
            if(i<6)Result6.setVisible(false);
            if(i<7)Result7.setVisible(false);
            if(i<8)Result8.setVisible(false);
        }catch(Exception e){
            e.printStackTrace();
        }
    }

