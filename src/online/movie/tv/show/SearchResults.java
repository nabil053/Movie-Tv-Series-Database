    private void Result2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Result2ActionPerformed
        String query = "SELECT content_type FROM content WHERE id="+Id2;
        ResultSet content = db.getContent(query);
        String content_type = null;
        try{
            while(content.next()){
                content_type = content.getString("content_type");
            }
        }catch(Exception e){
            e.printStackTrace();
        }
        if(content_type.equals("movie")){
            MoviePage m = new MoviePage(Id2);
            m.setVisible(true);
        }
        else if(content_type.equals("tv_series")){
            TVPage t = new TVPage(Id2);
            t.setVisible(true);
        }
        if(content_type.length()>0)this.dispose();
    }//GEN-LAST:event_Result2ActionPerformed

