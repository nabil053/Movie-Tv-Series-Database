    private void adminListAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminListAddActionPerformed
       String qstn,ans;
       qstn=faqQstn.getText();
       ans=faqAns.getText();
       
        if (qstn.equals("") || ans.equals("")) {
            JOptionPane.showMessageDialog(this, "please insert Data");
        }else{
            AdminDatabaseHandler db = new AdminDatabaseHandler();
                    db.connectDatabase();
                    boolean x = db.insertFAQ(qstn, ans);
                    if (x) {
                JOptionPane.showMessageDialog(this, "FAQ Inserted !!!");
            }else{
                    JOptionPane.showMessageDialog(this, "FAQ Failed !!!");
                    }
        }
    }//GEN-LAST:event_adminListAddActionPerformed

    private void adminListDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminListDeleteActionPerformed

        if (faqId.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Select a Row First");
        }else{
         AdminDatabaseHandler db=new AdminDatabaseHandler();
                    db.connectDatabase(); 
                     db.deleteFaq(Integer.valueOf(faqId.getText()));
                     JOptionPane.showMessageDialog(this, "Deleted");
                     //After data updated table will be refreshed
                        db.showFaq(faqTable, "");
                faqTable.setModel(new DefaultTableModel(null, new Object[]{"ID","Question","Answer"}));
                 db.showFaq(faqTable, faqSearchValue.getText());
                 faqId.setText("");
                 faqAns.setText("");
                 faqQstn.setText("");
                 
    }
    }//GEN-LAST:event_adminListDeleteActionPerformed

