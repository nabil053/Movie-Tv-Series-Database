    private void adminListUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminListUpdateActionPerformed
            
        String qstn,ans;
       qstn=faqQstn.getText();
       ans=faqAns.getText();
        int id = Integer.valueOf(faqId.getText());
        
        if (qstn.equals("") || ans.equals("") || id==0) {
            JOptionPane.showMessageDialog(this, "Please select a field to update data");
        }else{
                   AdminDatabaseHandler db = new AdminDatabaseHandler();
                    db.connectDatabase(); 
                     db.updateFaq(id, qstn, ans);
                     JOptionPane.showMessageDialog(this, "Updated");
                     //After data updated table will be refreshed
                       db.showFaq(faqTable, "");
                faqTable.setModel(new DefaultTableModel(null, new Object[]{"ID","User Name","Email","Mobile","Password","Country","Gender","Type"}));
                 db.showFaq(faqTable, faqSearchValue.getText());
                 faqId.setText("");
                 faqAns.setText("");
                 faqQstn.setText("");
 
        }
        
    }//GEN-LAST:event_adminListUpdateActionPerformed

