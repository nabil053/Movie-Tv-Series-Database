    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String usrpass;

        usrname = userName.getText().trim();
        usrpass = userPassword.getText().trim();

        if (usrname.equals("") || usrpass.equals("")) {
            JOptionPane.showMessageDialog(null, "Empty Fields");
        } else {
            try {
                AdminDatabaseHandler db = new AdminDatabaseHandler();
                db.connectDatabase();

                ResultSet resultSet=db.userLoginQuery(usrname,usrpass);

                if (resultSet.next()) {

                    ResultSet rs = db.getIsSubscribeValue(usrname);
                    if (rs.next()) {
                        String isSubscribed=rs.getString("is_subscribed");
                        if (isSubscribed.equals("no")) {
                            offerSubscriptionPlan("Your Not Subscribed in Any Plan. Do you want to Subscribe?");
                        }else if(isSubscribed.equals("pending")){
                            JOptionPane.showMessageDialog(null, "Please verify your account");
                            VerifyUser verifyUser = new VerifyUser();
                            verifyUser.setVisible(true);
                            verifyUser.pack();
                            verifyUser.setLocationRelativeTo(null);
                            verifyUser.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                            this.dispose();
                        }else if(isSubscribed.equals("yes")){
                            subscriptionRangeCheck(usrname);
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "username and password do not match");
                    userName.setText("");
                    userPassword.setText("");
                }
            } catch (Exception e) {
            }

        }
    }//GEN-LAST:event_loginButtonActionPerformed

