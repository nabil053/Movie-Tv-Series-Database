    private void subscriptionRangeCheck(String name){
            try{
                
             Calendar nowTime = Calendar.getInstance(); // creates calendar
                //nowTime.setTime(new Date());
            // nowTime.add(Calendar.DAY_OF_MONTH, 8); // adds one hour
             
             
            AdminDatabaseHandler handler = new AdminDatabaseHandler();
            handler.connectDatabase();
            ResultSet rs = handler.getSubscriberDates();

                if (rs.next()) {
                    
                    Calendar storeTime = Calendar.getInstance(); // creates calenda
                    storeTime.setTime(rs.getTimestamp(1));
                    
                    Calendar  endTime = Calendar.getInstance(); // creates calendar
                     endTime.setTime(rs.getTimestamp(2)); // sets calendar time/date
                   // endTime.add(Calendar.DAY_OF_MONTH, 7); // adds one hour
                    
                    if (nowTime.after(storeTime) && nowTime.before(endTime)) {
                        JOptionPane.showMessageDialog(this,"H1! "+name);
                         HomePage userView = new HomePage();
                               userView.setVisible(true);
                               userView.pack();
                               userView.setLocationRelativeTo(null);
                               userView.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                               this.dispose();
                    }else{
                    
                        AdminDatabaseHandler db=new AdminDatabaseHandler();
                        db.connectDatabase();
                        db.isSubscriptionOver(name);
                        
                        offerSubscriptionPlan("Your Subscription is over.Please Renew it!!!");
                        
                    }
                }else{
                JOptionPane.showMessageDialog(this,"Error!!!");
                }
            }catch(Exception e){
            
            }
    
    }
    
    
